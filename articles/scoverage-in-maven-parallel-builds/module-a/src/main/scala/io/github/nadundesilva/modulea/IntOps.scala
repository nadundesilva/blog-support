package io.github.nadundesilva.modulea

class IntOps(integer: Int) {
  def plus(otherInteger:     Int): Int = integer + otherInteger
  def minus(otherInteger:    Int): Int = integer - otherInteger
  def times(otherInteger:    Int): Int = integer * otherInteger
  def divideBy(otherInteger: Int): Int = integer / otherInteger
}
